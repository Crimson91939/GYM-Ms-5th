<h2>Overview</h2>
<p>Cardiovascular workouts are designed to balance three factors for maximum effectiveness and safety: frequency, intensity, and duration. You will also need to include a warmup period before you enter the target intensity period of your workout and a cool-down period before the end of your workout.
</p>

<h2>What Is Cardio Exercise?</h2>
<p>Common cardio exercises are brisk walking, running, cycling, swimming, rowing, and cross-country skiing. In the gym, cardio machines include treadmill, elliptical trainer, stationary cycles, stepping machine, rowing machine, and ski trainer.</p>

<p>Cardiovascular exercise raises your heart rate and breathing rate into the moderate-to-vigorous intensity level for 10 minutes or more.</p>

<p>Exercises that are done primarily to build strength, such as lifting weights, using weight machines, resistance exercise, and core workouts are usually not considered to be cardio exercises because they are structured to challenge strength not cardiovascular endurance. But you can structure certain weightlifting workouts to raise your heart rate and gain a cardio benefit. For example, a superset workout provides little rest in between exercises. As a result, your heart rate stays elevated throughout a series of complex exercises.</p>


