<h2>Warming up and Stretching</h2>
<p>Warming up before the more intense portion of your workout gets the blood flowing to your muscles and loosens you up. This is considered to be essential rather than just starting your workout at full effort.

<p>Traditionally, you are coached to stretch the primary muscles to be used in the workout after your warmup. There are several schools of thought on the use and effectiveness of stretching, with some experts advising to do a dynamic warm-up but not to do static stretches before cardio exercise.</p>


<h2>This is the traditional workout advice:</h2>

<p>A warmup of five to 10 minutes at low intensity will prepare your muscles for exercise and steadily raise your heart rate.</p>
<p>Start at an intensity of 50 to 60 percent of your maximum heart rate, doing whatever activity will be your workout method. If you are walking or running, start by walking or running at an easy pace that puts you into this heart rate zone—one where you can still carry on a full conversation.
Now is the time to stretch the muscles you will use in your workout. They are warmed up and may benefit from flexibility stretches or drills specific for the muscle groups you will be using in the workout.
Cooling Down</p>

<h2>After you have completed your workout in your target heart rate zone, you should cool down with five to 10 minutes of lower intensity. Follow these guidelines:</h2>

<p>Aim for a heart rate of 50 to 60 percent of maximum heart rate for 5 to 10 minutes for your cool down.
Traditionally, you would end your workout with gentle stretching of the muscles used in the workout. This is no longer universally recommended, but you can do it if you wish.

<h2>Frequency</h2>
<p>The minimum recommended amount of cardiovascular exercise 150 minutes per week of moderate-intensity aerobic activity or for 75 minutes per week of vigorous aerobic exercise, or a combination.1﻿ However, the sessions should be spread throughout the week.</p>

<p>The American College of Sports Medicine recommends cardiovascular exercise three to five days a week for most programs.</p>

<p>To give your body time to build and repair muscles alternate a day of an intense or long cardio exercise session with a day of rest or easy exercise such as walking and stretching or yoga.</p>

<h2>Duration</h2>
<p>How long should you exercise in each exercise session? For cardiovascular benefits, aim for 20 to 60 minutes in your target heart rate zone, apart from the time you spend in warmup and cool-down. At this duration, your body burns through its available glycogen energy and begins to burn stored fat. While you will still have the benefits of burning calories, if you exercise for less than 20 minutes in your zone, the best fitness benefits come from setting aside the 20 to 60 minutes to spend in the aerobic zone.</p>

<h2>Intensity</h2>
<p>When beginning a fitness program, concentrate on increasing your duration with good posture and form before you work on increasing the intensity of your workout. If you are using walking for your workout, work on increasing the number of minutes walked in each session. A general rule of thumb is that it is safest to increase this by 10 percent per week. Once you are walking comfortably and with good posture and form for 60 minutes at a time, then work on increasing the intensity by adding speed, hills, or intervals.</p>

