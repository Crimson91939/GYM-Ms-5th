<div>
    <h2>How To Warm Up For These Workouts</h2>
    <p>When you’re working with heavy free weights it’s vital to warm up before you start your session. Not only will you reduce your risk of injury, but you’ll also ensure you’re ready to excel from your first rep onwards, rather than struggling with your first few sets while your body gets accustomed to the idea you’re working out.</p>

        <p>A warm-up doesn’t have to take more than five to ten minutes, but it does have to be linked to the workout you’re about to do, so don’t just jump on the treadmill and then assume you’re ready to lift heavy weights.</p>
        
        <p>Start with this dynamic stretching routine to get your muscles moving, and then move on to some workout-specific exercises. With the workouts below, the simplest way to do this is to run through a round of the exercises you’re going to do, using very light weights or no weights at all. That way you’ll know for sure you’re working the exact muscles you’re about to test.</p>
        
        <p>When it comes to preparing for bodyweight exercises like diamond press-ups, you can do a shortened set during your warm-up round or opt for an easier variation like press-ups with your knees grounded. Remember you’re just aiming to get the muscles firing, not exhaust yourself before the workout proper begins.</p>
