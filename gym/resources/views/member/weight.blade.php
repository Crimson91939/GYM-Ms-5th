	<div>
	<p>It’s not essential to use free weights in your training to bulk up and burn fat, but if you use them in the right way, it’s fair to say dumbbells, kettlebells and the barbell can make building muscle easier. However, it does have to be in the right way. It’s easy to overestimate your abilities when selecting the weight for your workouts – and it’s always better to get the form right with a lighter weight, or even no weight at all, than get pulled out of shape straining to lift a weight you’re not ready for.</p>

	<p>The training plan below is a great way to get to grips with free weights. You’ll work with a barbell as well as dumbbells and kettlebells to challenge muscles all over your body, using foundational compound exercises like squats, lunges and deadlifts, as well as targeting specific muscle groups with isolated moves like biceps curls.</p>

	<p>Over the four weeks of the plan you’ll focus on movement patterns, rather than hitting certain muscle groups with your workouts. The first training session is built around pushing exercises, working the chest, quads, shoulders and triceps. Then in the second workout, you’ll do pulling moves, hitting your back, hamstrings and biceps. In the final workout, all the exercises involve rotating your body or resisting rotation.</p>

	<p>By training in this way you’ll build all-over functional strength, becoming stronger and leaner in a way that brings benefits to everyday life and when playing sports, as well as improving your performance in the gym.</p>
