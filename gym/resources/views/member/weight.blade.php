<h2>1> Dumbbell bench press</h2>

<div class="pt-4"><img src="{{asset('img/a2.jpg')}}" height="400px" width="800px;"></div>
<p class="pb-2"><b>Sets</b> 3 <b>Reps</b> 10 <b>Rest</b> 60sec</p>

<p></p><b>Why</b> The week kicks off with a double header of everyone’s favourite move – the bench press. You start with the dumbbell version because you’ll go a bit lighter than with a barbell, and it’s better for warming up your shoulders because you have to work harder to stabilise the joint.</p>

<p></p><b>How</b> Lie on a bench with your feet on the floor directly underneath your knees, holding the dumbbells above your chest. Lower them to your chest, then drive your feet hard into the floor and push the dumbbells back strongly to the start position.</p>
</div>


